package refactoring;

public class Sonata extends Car{
	@Override
	public void Start() {
		System.out.println("Sonata Start!");
		
	}

	@Override
	public void Drive() {
		System.out.println("Sonata Drive!");
		
	}

	@Override
	public void Break() {
		System.out.println("Sonata Break!");
		
	}

	@Override
	public void TurnOff() {
		System.out.println("Sonata TurnOff!");
		
	}

	@Override
	public void WashCar() {
		System.out.println("Sonata WashCar!");
		
	}


}
