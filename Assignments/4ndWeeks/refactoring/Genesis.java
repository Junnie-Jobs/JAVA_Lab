package refactoring;

public class Genesis extends Car{
	@Override
	public void Start() {
		System.out.println("Genesis Start!");
		
	}

	@Override
	public void Drive() {
		System.out.println("Genesis Drive!");
		
	}

	@Override
	public void Break() {
		System.out.println("Genesis Break!");
		
	}

	@Override
	public void TurnOff() {
		System.out.println("Genesis TurnOff!");
		
	}

	@Override
	public void WashCar() {
		System.out.println("Genesis WashCar!");
		
	}

}
