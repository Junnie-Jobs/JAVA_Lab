package refactoring;

public class Avante extends Car{

	@Override
	public void Start() {
		System.out.println("Avante Start!");
		
	}

	@Override
	public void Drive() {
		System.out.println("Avante Drive!");
		
	}

	@Override
	public void Break() {
		System.out.println("Avante Break!");
		
	}

	@Override
	public void TurnOff() {
		System.out.println("Avante TurnOff!");
		
	}

	@Override
	public void WashCar() {
		System.out.println("Avante WashCar!");
		
	}

}
