package refactoring;

public class Grandeur extends Car{
	
	@Override
	public void Start() {
		System.out.println("Grandeur Start!");
		
	}

	@Override
	public void Drive() {
		System.out.println("Grandeur Drive!");
		
	}

	@Override
	public void Break() {
		System.out.println("Grandeur Break!");
		
	}

	@Override
	public void TurnOff() {
		System.out.println("Grandeur TurnOff!");
		
	}

	@Override
	public void WashCar() {
		System.out.println("Grandeur WashCar!");
		
	}

}
